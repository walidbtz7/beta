                            être abandonnée ? Cette question revient au centre des débats au moment où Emmanuel Macron devait commencer d’égrener son plan d’action contre le « séparatisme islamiste » à Mulhouse, mardi 18 février. L’organisation du culte
                            musulman devrait être l’une des composantes de ce programme, et en particulier la question de la formation des cadres religieux. Celle-ci a été qualifiée de « priorité absolue pour l’islam de France » dans le communiqué qui
